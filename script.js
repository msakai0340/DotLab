function convertToBraille() {

  const input =
    document
      .getElementById("hiraganaInput")
      .value
      .trim();

  let result = [];

  let i = 0;

  while (i < input.length) {

    // 2文字取得（拗音優先）
    const twoChars =
      input.substring(i, i + 2);

    if (brailleMap[twoChars]) {

      result.push(
        brailleMap[twoChars]
      );

      i += 2;

    } else {

      const oneChar = input[i];

      if (brailleMap[oneChar]) {

        result.push(
          brailleMap[oneChar]
        );

      } else {

        result.push("?");
      }

      i++;
    }
  }

  document
    .getElementById("brailleResult")
    .textContent =
      result.join(" / ");
}
